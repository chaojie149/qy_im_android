package com.tongxin.caihong.bean;

public class AccountItem {
    int id;
    String name;
    int type;

    public int getId() {
        return id;
    }

    public void setId(int mId) {
        id = mId;
    }

    public String getName() {
        return name;
    }

    public void setName(String mName) {
        name = mName;
    }

    public int getType() {
        return type;
    }

    public void setType(int mType) {
        type = mType;
    }
}
